    // ðŸ§© Method to delete staff
private void removeStaff() {
    String staffId = jTextField1.getText().trim();

    if (staffId.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a Staff ID!");
        return;
    }

    try {
        // Connect to database
        java.sql.Connection con = java.sql.DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/library_system", "root", "Ayushi@2005");

        // Check if staff exists
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery("SELECT * FROM staffs WHERE staff_id = '" + staffId + "'");

        if (!rs.next()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Staff ID not found!");
            con.close();
            return;
        }

        // Delete the staff
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this staff?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            st.executeUpdate("DELETE FROM staffs WHERE staff_id = '" + staffId + "'");
            javax.swing.JOptionPane.showMessageDialog(this, "Staff deleted successfully!");
        }

        con.close();

        // Reopen updated StaffDetails page
        new StaffDetails().setVisible(true);
        this.dispose();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error while deleting: " + e.getMessage());
    }
}


 

